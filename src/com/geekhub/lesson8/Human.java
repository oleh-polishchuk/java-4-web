package com.geekhub.lesson8;

public class Human {

    private Integer height;
    private String gender;
    private Integer age;
    private Integer weight;

    public Human() {}

    public Human(Integer height, String gender, Integer age, Integer weight) {
        this.height = height;
        this.gender = gender;
        this.age = age;
        this.weight = weight;
    }
}
