package com.geekhub.lesson8;

public class Car {

    private String color;
    private Integer maxSpeed;
    private String type;
    private String model;

    public Car() {}

    public Car(String color, Integer maxSpeed, String type, String model) {
        this.color = color;
        this.maxSpeed = maxSpeed;
        this.type = type;
        this.model = model;
    }
}
